/*
 ? Array.push - Adds value at the end of the Array
 ? Array.unshift - Adds the value at start of the array
 ? Array.pop - Removes the value at the end of the array
 ? Array.shift - Removes the value at the start of the array
*/
var array = [1,2,3,4]

array.push(5)
array.push(6)
array.push(7)
array.unshift(0)
array.unshift(-1)
array.unshift(-2)
array.pop()
array.pop()
array.shift()
array.shift()
